import Mock from "mockjs";

///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


/**
 * Mock.Random();
 *
 *
 */

let Random = Mock.Random;


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


/**
 * Random.region();
 *
 *
 * 随机生成一个（中国）大区
 */

console.log("Random.region(): ", Random.region());


//-------------------------------------------------------------------------------------------------------------------//


/**
 * Random.province();
 *
 *
 * 随机生成一个（中国）省（或直辖市、自治区、特别行政区）
 */

console.log("Random.province(): ", Random.province());


//-------------------------------------------------------------------------------------------------------------------//


/**
 * Random.city(prefix?);
 *
 *
 * @prefix: 布尔值。指示是否生成所属的省
 *
 *
 * 随机生成一个（中国）市
 */

console.log("Random.city(): ", Random.city());


//-------------------------------------------------------------------------------------------------------------------//


/**
 * Random.county(prefix?);
 *
 *
 * @prefix: 布尔值。指示是否生成所属的省、市
 *
 *
 * 随机生成一个（中国）县
 */

console.log("Random.county(): ", Random.county());


//-------------------------------------------------------------------------------------------------------------------//


/**
 * Random.zip();
 *
 *
 * 随机生成一个邮政编码（六位数字）
 */

console.log("Random.zip(): ", Random.zip());


